clc;
clear;
close all;
f = -250:0.001:250;
Pi = @(x) ((x >= -0.5) & (x <= 0.5));
Y = 0.5 * Pi(f - 200) + 0.5 * Pi(f + 200) - 0.5 * sign(f - 200) .* Pi(f - 200) + 0.5 * sign(f + 200) .* Pi(f + 200);
figure(1);
plot(f, Y);
xlabel('f / Hz', 'fontsize', 16);
ylabel('Amplitude / a.u.', 'fontsize', 16);
xlim([-250,250]);
figure(2);
plot(f, Y);
xlabel('f / Hz', 'fontsize', 16);
ylabel('Amplitude / a.u.', 'fontsize', 16);
xlim([-202,-198]);
figure(3);
plot(f, Y);
xlabel('f / Hz', 'fontsize', 16);
ylabel('Amplitude / a.u.', 'fontsize', 16);
xlim([198,202]);
